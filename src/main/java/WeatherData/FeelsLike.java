package WeatherData;

public class FeelsLike {
    Double day;
    Double night;
    Double eve;
    Double morn;

    public Double getDay() {
        return day;
    }

    public Double getNight() {
        return night;
    }

    public Double getEve() {
        return eve;
    }

    public Double getMorn() {
        return morn;
    }
}
