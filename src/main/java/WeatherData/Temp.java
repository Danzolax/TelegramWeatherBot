package WeatherData;

public class Temp {
    Double day;
    Double min;
    Double max;
    Double night;
    Double eve;
    Double morn;

    public Double getDay() {
        return day;
    }

    public Double getMin() {
        return min;
    }

    public Double getMax() {
        return max;
    }

    public Double getNight() {
        return night;
    }

    public Double getEve() {
        return eve;
    }

    public Double getMorn() {
        return morn;
    }
}
